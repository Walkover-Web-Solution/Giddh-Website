						<h2 class="text-uppercase"><span class="d-inline-block d-md-none">7.</span>GST is advantageous to business people in the following ways</h2>

						<h3>Unification</h3>
						
						<p>The biggest relief that the implementation of the GST has given all business people is freedom from the cascading tax regime. The earlier regime had various taxes that were levied by different authorities. Each tax had different compliance requirements. There were many tax heads that had to be calculated, collected and paid depending on the type of goods. Services were taxed differently. To add to the complications, the states levied taxes differently making doing businesses in different states rather complicated. The levies of taxes on interstate transport of goods was another time-consuming process that often saw transported goods being held up at the border check post queues. The taxes under the older regime were by different authorities making the claiming of input tax credit difficult. So, at each stage, the entire value of the goods or services were charged taxes. </p>

						<p>GST has unified the collection of taxes under one body making it very easy to claim input tax credits. So at each stage, the business person only has to bear the tax for the value addition for the goods. The interstate taxes are collected by the centre eliminating the cumbersome process of transporting goods across the state and being levied varying taxes depending on each stage. </p>

						<p>Some of the heads under which taxes were collected in the previous regime were: </p>
						<ul>
							<li>Duties of Excise</li>
							<li>Central Excise Duty</li>
							<li>Additional Duties of Customs</li>
							<li>Additional Duties of Excise</li>
							<li>Special Additional Duty of Customs</li>
							<li>Central Sales Tax</li>
							<li>Cess</li>
							<li>State VAT</li>
							<li>Purchase Tax</li>
							<li>Entertainment Tax</li>
							<li>Luxury Tax</li>
							<li>Entry Tax</li>
							<li>Taxes on lotteries, betting, and gambling</li>
							<li>Taxes on advertisements</li>
						</ul>

						<p>All these taxes have been replaced by the GST scheme with a simple division of taxes as Central Goods & Service Tax (CGST) to the centre, State Goods & Services Tax (SGST) to the state and the Integrated Goods & Services Tax (IGST) for interstate business. </p>

						<h3>Example:</h3> 

						<p>If we take the example of a goods item that was manufactured and supplied to the wholesaler @ Rs. 100 attracting 10% of tax. This would amount to Rs.110. This is then supplied to a retailer after increasing the price to Rs. 150 again attracting 10% tax making it valued at Rs. 165. The retailer again adds Rs. 15 to the value making it Rs. 180 plus 10% tax making it Rs. 198. So, everybody adds their profit margin and the final buyer pays tax on the entire amount including all the taxes previously paid making it a tax on a tax. </p>

						<p>Under the GST regime, the very same goods item would be supplied to the wholesaler @ Rs. 100 attracting 10% of GST. This would amount to Rs. 110. This is then supplied to a retailer after increasing the price to Rs. 150 again attracting 10% tax making it valued at Rs. 165. But after claiming the input tax credit the actual tax burden is only on the added value of Rs. 50. The retailer again adds Rs. 15 to the value making it Rs. 180 plus 10% tax making it Rs. 198. But the actual tax borne by the retailer is only on the Rs. 15. So, even when everybody adds their profit margin they each pay the tax only for the amount that they have added and get the input tax credit for the tax that has already been paid on the incoming item. The final buyer is the person who is relieved of bearing the burden of everybody’s value addition as well as the tax on the tax paid.</p>

						<h3>Threshold and Composition Schemes</h3>							

						<p>GST has a threshold below which businesses are given certain exemptions that are aimed at encouraging startups and small businesses. Smaller businesses would find it very difficult to close books and file their taxes every month and so they have been given the benefit of being able to file every quarter thereby reducing the work involved. Under the composition scheme, the dealer does not have to worry about the supplier filing his GST to avail input tax credit. </p>

						<p>For example, if a normal taxpayer is collecting an 18% GST and then claiming input credit a composition scheme taxpayer would only be levied 2% GST but would not be claiming an input tax credit at all. So, the composition scheme taxpayer would not get his capital locked up awaiting the input tax credit payment. This makes it easier for the smaller business owner to do businesses without worrying about compliance and penalties.</p>

						<h3>Online Payments</h3>							
						
						<p>The system of paying the <a target="_blank" href="https://giddh.com/gst/">GST online</a> makes the whole system more transparent and easy for the businessperson to manage without the need for any additional help. This will eliminate the need for any middlemen or corruption at any level. It makes the entire system easy to access verify and track. Having one place to manage taxes is a huge change from the previous cascading tax system.</p>

						<h3>Easier to Comply</h3>

						<p>The earlier tax regime had many different heads and authorities that levied and collected taxes. To make matters even more complicated, the system was totally different in each state. For a person who was starting a business especially in more than one state, it would mean a whole lot of paperwork and red tape involved in taxation. It also meant that it was very difficult to comply with each and every aspect of the taxes. With the different entry taxes and interstate levies added to the tax structure, it was a very expensive and cumbersome task to run a nationwide business.</p> 

						<h3>Logistics Made Easy</h3>

						<p>The past scenarios of trucks and transport vehicles getting log jammed at interstate check posts sometimes for hours and days on end is in the past. In extreme cases, perishable goods would spoil and be unfit for sale due to the holdup. Interstate and countrywide logistics are now made simple and there is no fear of problems with paperwork and compliance differing in each state. The entire process is absolutely transparent and the whole country is now one level marketplace for business people. </p>

						<h3>Regulation and Better Coverage</h3>

						<p>With the unorganised sector being the input providers for many manufacturing industries, there is now better coverage and compliance under GST. With the ability to claim input tax credit, the entire supply chain including the unorganised sectors is getting covered under the GST scheme. This is excellent for the revenues for the government as well as for the entire financial aspect of the industries getting their taxes and books maintained properly. This helps smaller businesses maintain liquidity. </p>

