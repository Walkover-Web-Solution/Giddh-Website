						<h3>GSTR-3B</h3>	

						<p>The <a target="_blank" href="https://www.gst.gov.in/download/gstr3b">GSTR 3B</a> is a simple return that has the overview or summary of the details of the input tax credit as well as the outward supplies. It is a self-declared GST form. The tax liability of this form must be paid by the GST return due date of the 20th of the subsequent month. It is necessary to file this form even if there have been no transactions. The GSTR 3B form does not have to be filed by </p>

