						<h2 class="text-uppercase"><span class="d-inline-block d-md-none">11.</span>Checklist for businesses to be GST compliant</h2>

						<p>To make your business GST compliant you will have to ensure the following</p>

						<ul>
							<li>Update yourself on the required frequency and due dates of the filing of GST returns to avoid penalties and to maintain a good rating.</li>
							<li>Update the transaction classifications using <a target="_blank" href="http://etdut.gov.in/exciseonline/misc/gst_hsn-sac_codes.pdf">HSN/SAC codes</a> as well as interstate transactions and intrastate transactions. </li>
							<li>Record the place and time of supply clearly</li>
							<li>Update the tax rate slabs applicable</li>
							<li>Register all locations, factories, outlets, warehouses and supply points.</li>
							<li>Get registrations for different business verticals and different jurisdictions (states).</li>
							<li>Offset existing inventory against GST.</li>
							<li>Update chart of accounts</li>
							<li>Reconcile accounts as per the new schedule</li>
							<li>Change the marketing and sales policies to match with GST</li>
							<li>Update the logistics and the payment models to reflect the destination-based GST.</li>
							<li>Update all supply chain agreements and contracts to reflect the obligations that GST entails.</li>
							<li>Update all the required information that is required by GST for input and output filing.</li>
						</ul>

