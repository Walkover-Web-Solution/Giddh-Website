						<h1 class="text-uppercase">What is GST</h1>

						<p>GST stands for Goods and Service Tax. The implementation of GST is one of the biggest tax reforms in India and for the first time, the nation has a uniform tax structure under a single authority. It was implemented as a bill passed in the Rajya Sabha on 03.08.2016 and then in the Lok Sabha on 08.08.2016. It was notified as a Constitution Act (101st Amendment ), 2016 on 08.09.2016. GST is a tax that is applicable to goods as well as services. Goods are defined as all commodities, materials and articles while services are anything other than goods. </p>

						<p>The GST law has unique principles and is inspired by the GST structures of other countries such as Australia, the European Union as well as the international guidelines recommended by the OECD (Organisation for Economic Co-operation and Development). Since the country allows both the centre and the states to levy taxes, the GST structure has a dual system that allows the states and the central taxes to function independently. </p>

