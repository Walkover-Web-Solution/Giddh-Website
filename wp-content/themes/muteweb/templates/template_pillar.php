						<h2 class="text-uppercase"><span class="d-inline-block d-md-none">10.</span>FAQs</h2>

						<h3>How do I get a GST registration?</h3>
						<p>A person or entity can make a GST application for GST enrollment using the portal of Goods & Services Tax.</p>

						<h3>How to file GST return?</h3>
						<p>GST returns are to be filed online through the software or apps provided by Goods and Service Tax Network (GSTN). You can file GST online at <a target="_blank" href="https://www.gst.gov.in/">www.gst.gov.in</a> . </p>

						<h3>Can a person have more than one GST registration?</h3>
						<p>A person who has multiple businesses in different verticals within each state can have a different GST for each vertical within that state.</p>

						<h3>What is the composition scheme?</h3>
						<p>The GST regime has created a composition scheme to reduce the burden of taxes and the time spent computing and calculating taxes for small businesses. At present, the scheme is applicable to businesses that have a turnover that is less than Rs. 75 lakhs. They have to pay a very nominal tax of .5% or 1% of <a target="_blank" href="http://www.gstcouncil.gov.in/cgst">CGST</a> and <a target="_blank" href="http://www.gstcouncil.gov.in/sgst-act">SGST</a> each. This is only applicable to sellers of goods. It is not applicable for sellers of services, eCommerce sellers, eCommerce operators and interstate sellers.</p>

						<h3>Who is ineligible for the composition scheme?	</h3>

						<p>The composition scheme does not apply to the following</p>

						<ul>
							<li>Service providers, i.e., sellers of services (exception for restaurant owners serving non-alcoholic beverages and food)
							Sellers of non-taxable goods</li>
							<li>eCommerce sellers</li>
							<li>eCommerce operators</li>
							<li>Interstate sellers</li>
							<li>Suppliers of certain goods such as ice cream, pan masala and tobacco.</li>
						</ul>

						<h3>What is a bill of supply?</h3>
						<p>A person who is under the composition scheme cannot claim the tax credit and hence has to issue a bill of supply. </p>

						<h3>Can a regular registration be converted into a composition scheme registration?</h3>
						<p>A person desiring to convert from a normal registration to composition scheme would have to file an intimation in the GST form <a target="_blank" href="https://www.webtel.in/Image/GST_CMP_02%20.pdf">GST CMP-2</a> and <a target="_blank" href="https://www.webtel.in/Image/GST_ITC_-_3.pdf">ITC-03</a> within 60 days of the beginning of the financial year.</p>
						 
						<h3>Can a composition scheme registration be withdrawn?</h3>
						<p>Yes. The <a target="_blank" href="https://www.webtel.in/Image/GST_CMP_04.pdf">GST CMP-04</a> form should be filled before withdrawal from the composition scheme.</p>

						<h3>Is there a format to create a GST invoice?</h3>
						<p>Yes, there is a recommended format to create a GST invoice.</p>
						
						<h3>What is an HSN code?</h3>
						<p><a target="_blank" href="http://wbcomtax.nic.in/GST/GST_FAQ/GST_HSN_CODES.pdf">HSN</a> is the Harmonized System of Nomenclature. It is a 6 digit code developed by the World Customs Organization (WCO) that is used worldwide for the classification of products. It has been in effect since 1988. </p>

						<h3>Do you need a GSTIN for the place from which the goods are originating?</h3>
						<p>Yes, the GSTIN has to be at the place where the goods are originating. However, GST is a destination-based tax. So, the tax would accrue to the place that has jurisdiction over the place of supply.</p>
						 
						<h3>Are the GSTIN & TIN numbers different?</h3>
						<p>Yes, the TIN number is registered under the VAT law and the GSTIN number under the GST law.</p>

						<h3>How will GST be applied to imports?</h3>
						<p>Imports of goods and services will be taxed as interstate supplies thereby attracting IGST based on the destination-based nature of the tax. The SGST tax will accrue to the state being supplied/consumed. </p>

						<h3>How will GST be applied to exports?</h3>
						<p>There will be no tax that will be payable on exports. Exports are treated as zero-rated supplies and the exporters can claim input tax credits. The exporter can pay the output tax and claim <a target="_blank" href="http://cbic.gov.in/resources//htdocs-cbec/gst/Refund-Compendium-020719.pdf;jsessionid=8158485FB38A9E3E7916457CBFCD947A">IGST refund</a> or export without IGST and claim input tax credit refund.</p>

						<h3>What is to be done when goods are returned by the purchaser?</h3>
						<p>When goods are returned, the supplier issues a credit note to the recipient. This note will be matched with the reduction in the input tax claim. </p>

