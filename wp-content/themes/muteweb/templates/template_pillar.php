						<h2 class="text-uppercase"><span class="d-inline-block d-md-none">8.</span>Understanding SGST, CGST & IGST</h2>

						<p>The division of the taxes into SGST, CGST and <a target="_blank" href="http://wbcomtax.nic.in/GST/GST_FAQ/Integrated_GST_(IGST).pdf">IGST</a> is very straightforward.</p>

						<h3>Central Goods & Service Tax (CGST)</h3>

						<p>CGST stands for the Central Goods & Service Tax (CGST).  The CGST is the levy that is paid to the central government. It is governed by the Central Goods & Services Tax Act 2016. The taxes that were levied by the centre in the former tax regime have all been replaced by this one CGST. It functions under one central authority.</p>

						<h3>State Goods & Services Tax (SGST)</h3>

						<p>SGST is the State Goods & Services Tax or SGST. It is paid to the state government and it replaces the taxes such as octroi, entry tax, state sales tax, luxury tax, etc that were collected by the state governments. All the levies that are collected under this head belong to the state government and it is controlled by the state authority that in turn is governed by the central GST body. It is this tax reform in particular that has given a big relief to business people. It was not practically achievable in the earlier regime to collect an input tax benefit when the tax on the input and the tax on the sale were levied and collected by two totally different bodies. In some cases, the tax that was collected on the input would not even exist in the state where the next sale was being made. Having the SGST of all the states under the control of one central body has made a straightforward and simple process to claim the input tax benefit without any special regard to the source and destination states. It has truly made the nation one unified level playing field for all companies and individuals.</p>

						<h3>Integrated Goods & Services Tax (IGST)</h3>

						<p>The IGST is the Integrated Goods & Services Tax is applicable to interstate supply of goods and services. It is collected by the centre and given to the respective states. It is governed by the central body. This tax has revolutionised the entire logistics and interstate scenario of the country. When the earlier regime had many interstate check posts and different taxes applied to enter each state, it resulted in bottlenecks and holding up of goods at borders. There was a lot of paperwork and the entire process was risky and time-consuming. There was no telling how long the goods would take to cross the state border. This tax has made the state borders much easier to cross and made the entire country one market.</p>

						<h3>Who should register for GST?</h3>

						<p>The implementation and functioning of the GST regime are wholly dependent on proper coverage of every person involved at every stage of the supply chain of any goods or services. Therefore, the proper coverage and <a target="_blank" href="https://giddh.com/blog/gst-and-its-impacts-on-indian-smes-working-capital/">new registration of GST</a> for all applicable businesses and individuals are very important. To register for GST, business owners can apply online. <a target="_blank" href="https://giddh.com/signup/">Registration under GST</a> is required for the following categories of people/businesses:</p>
						<ul>
							<li>Any entity that was previously registered for any tax whether state or central.</li>
							<li>Businesses with a turnover that is above the threshold limit set.</li>
							<li>A casual taxable person.</li>
							<li>A non-resident taxable person</li>
							<li>Supply agents and Service Distributors</li>
							<li>Reverse Charge Taxpayers</li>
							<li>E-Commerce aggregator suppliers</li>
							<li>Provider of online database access or information retrieval to a person in India from outside India (other than a registered taxable person)</li>
						</ul>

						<h3>Documents required for new GST Registration</h3>

						<p>New GST registration can be made online. The documents that are required to apply for <a target="_blank" href="https://giddh.com/gst/">GST online</a> registration are </p>

						<ul>
							<li>The PAN of the applicant</li>
							<li>The Aadhaar card of the applicant</li>
							<li>Incorporation certificate or registration proof of the business</li>
							<li>ID proof and address proof of the individual or the promoters/Directors of the business</li>
							<li>Address proof of the business</li>
							<li>Cancelled cheque or bank account statement</li>
							<li>Digital signature</li>
							<li>Board resolution or letter of authorisation to authorise the authorised signatory</li>
						</ul>

						<h3>What is GST Return?</h3>

						<p>GST returns are the tax returns that are filed through GST forms by a person with the tax authorities with details of income. These details are then used to calculate the tax liability of that person. This is a document that gives details of transactions that include:</p>

						<ul>
							<li>Sales</li>
							<li>Output GST (Which is the GST charged on a sale)</li>
							<li>Purchases</li>
							<li>Input tax credit (which is the GST paid on purchases)</li>
						</ul>

						<p>Therefore, the invoices that are made for purchases, as well as the sales that are filed for GST online, should be GST compliant.</p>

						<p>This has to be done every month for businesses that are not composition dealers. So, when the taxes filed every month are towards the CGST and the SGST it makes two GST returns a month and then the annual returns making it a minimum of a total of 26 GST returns per year.</p>
