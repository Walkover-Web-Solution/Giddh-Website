						<h2 class="text-uppercase"><span class="d-inline-block d-md-none">2.</span> Types of GST</h2>

						<p>The three main types of GST are Central Goods & Service Tax (CGST), State Goods & Services Tax (SGST) and the Integrated Goods & Services Tax (IGST). A fourth type is the Union Territory Goods & Services Tax (UTGST). In India, taxes are levied by the centre and state. The jurisdiction for the centre and the states to levy taxes are concurrent.</p>

						<h3>Central Goods & Service Tax (CGST)</h3>

						<p>The Central Goods & Service Tax (CGST) is governed by the Central Goods & Services Tax Act 2016. It is the tax that replaces all the taxes and duties that were formerly levied by the centre under various heads such as service tax, excise duties, customs duties etc. The taxes that are collected under the CGST belong to the central government and can be amended by a body that functions under the centre. The input tax under this head is given to the state governments and they have to pay the CGST to utilise it.</p> 

						<h3>State Goods & Services Tax (SGST)</h3>

						<p>The GST that is collected by the states is called the State Goods & Services Tax or SGST. It replaces the taxes and levies that were made by each of the state governments earlier including those such as entry tax, octroi, state sales tax, luxury tax, etc. The revenue collected under the SGST belongs to the state government and function under the state authority for the SGST. This state authority is governed by the central GST body. </p>

						<h3>Integrated Goods & Services Tax (IGST)<h3>

						<p>The IGST or the Integrated Goods & Services Tax is applicable to the goods and services that are supplied from one state to another. This tax is collected by the centre and can be changed whenever required by the Goods and Services Tax Council of India.</p>

						<h3>Union Territory Goods & Services Tax (UTGST)<h3>

						<p>The tax that replaces all the taxes and levies that were formerly made by the Union Territories is called the Union Territory Goods & Services Tax or the UTGST. </p>

						<h3>The Four Tiers Of GST<h3>

						<p>GST has been formulated with a four-tiered structure that has slabs with different rates, namely; Zero Rate, Lower Rate, Standard Rates and Higher Rate.</p>

						<h3>Zero Rate - 0%<h3>

						<p>Certain goods and services that are considered to be essentials have been put under the zero-rate slab of the GST structure. They attract no GST. These goods and services include unpacked food grains, milk, health and educational services.</p>

						<h3>Lower Rate - 5%<h3>

						<p>Certain other essential items attract a lower rate of GST to protect people from the effect of inflation on daily essentials. These are tea, sugar, spices, edible oils, coal, cotton fabric etc. This lower rate is currently at 5%. </p>

						<h3>Standard Rate- 12% and 18%<h3>

						<p>There are two standard rates of 12% and 18 % that are applicable based on the category of goods and services. The 12% rate applies to items such as mobile phones and processed food while the 18% rate applies to computers, capital goods and industry intermediaries. </p>

						<h3>Higher Rate - 28%<h3>

						<p>The higher rate of GST is applicable on white goods such as high-end motorcycles, small cars, washing machines, refrigerators, etc. </p>

						<h3>Additional Cess<h3>

						<p>To prevent certain goods such as aerated drinks, tobacco etc from being too cheaply available the government has levied an additional cess over and above the higher rate of 28%. This will apply to demerit goods such as pan masala, tobacco, coal, motor vehicles and aerated drinks. </p>	

