						<p>The <a target="_blank" href="http://gstblog.i-tax.in/Forms/Return/GSTR-4.pdf">GSTR-4</a> is the GST returns form that is used by the composition dealer. It is filed quarterly and by the 18th of the month succeeding the quarter. When the ordinary taxpayer files three returns, one in every month of the quarter, the composition dealer files this return every 3 months. Late filing attracts a daily penalty. One cannot file the GSTR-4 for a quarter without having filed the GSTR-4 for the previous quarter. </p>

