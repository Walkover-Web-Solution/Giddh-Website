						<h2 class="text-uppercase"><span class="d-inline-block d-md-none">5.</span>Life before GST</h2>

						<p>The setting up of any business under the previous tax system required many different taxes and procedures under different authorities both central and state. The state-level taxes were different in each state. This made both setting up businesses and calculating and paying taxes difficult. One would have to hire an expert to step in and provide expert advice. Taxes were calculated on the value of the goods or the services that were being sold. </p>

						<p>GST only taxes the value addition at each stage of the supply chain starting with the process of raw material through stages of manufacturing distribution and final sale. GST also gives credit for the input tax that has been paid at the previous stage of the supply chain. So, if a person pays the tax that applies to his/her sale and then claims the input credit of the tax that he/she had paid to procure the item sold, it is only the actual difference amount that the person is actually paying.</p>

						<h3>Example:</h3>
						<p>We can illustrate this with an example of a product XYZ. </p>

						<h4>Previous Tax Regime</h4>

						<p>In this example, we presume that the VAT in the first state is 10% and the VAT in the second state is 12%. The interstate central sales tax (ICST) is presumed to be 12%. For the sake of this example, we also presume that the manufacturer of XYZ has paid all the taxes for the manufacturing process of XYZ. </p>

						<p>The manufacturer of XYZ sells his product to a wholesaler in the same state at Rs.100 per piece plus taxes. The taxes in the same state are a VAT of 10%. This makes the amount that the wholesaler pays for each item of XYZ Rs. 110. The wholesaler then increases the rate of XYZ to Rs. 200 and sells it to a retailer in another state. This attracts an interstate central sales tax (CST) of 12 %. This makes the rate paid by the retailer for each XYZ Rs. 224. The retailer decides to mark the selling price of XYZ at 25% more than he purchased it making it Rs. 280 per piece. When he sells it to the customer it attracts a VAT of 12%. The customer pays Rs. 313.60 for XYZ.</p>

						<p>Here we see that as there are different values of VAT that are levied by different authorities in each state it would be impossible to calculate and claim the input tax credit. </p>

						<h4>GST Regime</h4>

						<p>Now we can take the same example under the GST system. The manufacturer sells XYZ to the wholesaler in the same state at Rs. 100 per piece plus a GST of 10% making the amount paid by the wholesaler Rs. 110. This wholesaler then increases the rate to Rs. 200 and sells the product to a retailer in another state attracting an IGST of 10% making the price paid by the retailer Rs. 220. The retailer increases the price by 25% marking it at Rs. 275. The end customer buys it at Rs. 275 plus 10% GST amounting to Rs. 302.50. </p>

						<p>Here we can see that the GST levied on the product XYZ has been the same 10% throughout. The interstate IGST was only levied when the product was sold between two states. Each person at each stage of the supply chain can avail input tax credit on the SGST and CGST. The calculation and claiming of the input tax credit at every stage is much easier when it is uniform and under the same authority. This will benefit the end customer if the sellers choose to pass on this benefit to them. It benefits the manufacturer, wholesaler and the retailer as well as they can claim an input tax credit for their purchases and only bear the burden of the additional tax that their value addition attracts. </p>
