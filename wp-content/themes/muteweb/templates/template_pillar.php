						<h2 class="text-uppercase"><span class="d-inline-block d-md-none">9.</span>What are the different types of GST Returns?</h2>

						<p>The different types of GST returns are categorised according to the numbers of the GST forms.</p>

						<h3>GSTR-1</h3>

						<p>The <a target="_blank" href="http://gstblog.i-tax.in/Forms/Return/GSTR-1.pdf">GSTR-1 form</a> for GST returns is for the outward sale or supply of goods and services. It is to be filed every month. The last date for filing is the 11th of the subsequent month. This GST form has 13 sections. There is a special feature for businesses that have sales of up to Rs.1.5 crores who can file every quarter instead of the monthly returns that have to be filed with businesses with sales exceeding 1.5 crores. This form has to be filed even when there have been no transactions in that month. </p>

						<p>Since bulk uploads are time-consuming it is possible to upload invoices throughout the month regularly. These can be changed before the filing. This return once filed cannot be modified. Instead, it can be adjusted only during the next return. A daily late fee is charged for the CGST and the SGST from the date after the due date of the returns. The actual payment of the tax is made after the filing of the GSTR-3B.</p>

						<p>The following people are exempted from filing this return:</p>

						<ul>
							<li>Composition Dealers (who will have to file GSTR-4 instead on a quarterly basis)</li>
							<li>Input Service Distributors</li>
							<li>Non-resident taxable person</li>
							<li>Taxpayer liable to collect TCS or deduct TDS</li>
							<li>Suppliers of online information and database access or retrieval services (OIDAR)</li>
						</ul>

						<h3>GSTR-2</h3>

						<p>GSTR-2 is the return that is filed listing the details of the inward taxable goods and services. It is easy to fill as it auto-populated by the details that were filled in by the seller or supplier of goods and services. It is meant to verify that the details that are given for the sale by the seller and the details given by the purchaser match each other. This is called buyer-seller reconciliation. It is necessary to be able to get the input tax credit. It should be filed by the fifteenth of the subsequent month. It is essential to file the <a target="_blank" href="https://www.webtel.in/Image/Form%20GSTR_2_New.pdf">GSTR-2</a> as the GSTR-3 cannot be filed without the GSTR-2 thus attracting penalties. The GSTR-2 has to be filed even when there has been no transaction in that month. The following people do not have to file GSTR-2:</p>

						<ul>
							<li>Composition Dealers (who will have to file GSTR-4 instead on a quarterly basis)</li>
							<li>Input Service Distributors</li>
							<li>Non-resident taxable person</li>
							<li>Taxpayer liable to collect TCS or deduct TDS</li>
							<li>Suppliers of online information and database access or retrieval services (OIDAR)</li>
						</ul>

